package model;

public class RelationTrad {
	String id,trad;

	public RelationTrad(String id, String trad) {
		
		this.id = id;
		this.trad = trad;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTrad() {
		return trad;
	}

	public void setTrad(String trad) {
		this.trad = trad;
	}
	

}
